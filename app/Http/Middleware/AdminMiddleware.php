<?php

namespace App\Http\Middleware;

use Closure;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Symfony\Component\HttpFoundation\Response;

class AdminMiddleware
{
    // Middleware для проверки административных прав пользователя
    public function handle(Request $request, Closure $next): Response
    {
        // Проверяем, авторизован ли пользователь
        if (!Auth::check()) {
            return redirect('/login')->with('error', 'Пожалуйста, войдите в систему.');
        }

        // Проверяем, является ли пользователь администратором
        if (Auth::user()->role !== 'admin') {
            return redirect('/consultations')->with('error', 'Доступ запрещен. Требуются права администратора.');
        }

        return $next($request);
    }
}